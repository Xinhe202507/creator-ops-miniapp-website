document.body.addEventListener("click", (event) => {
  const closeTarget = event.target.closest("[data-close-modal]");
  if (closeTarget) closeModal();

  const closeSkuTarget = event.target.closest("[data-close-sku-modal]");
  if (closeSkuTarget) closeSkuModal();

  const closeVideoTarget = event.target.closest("[data-close-video-modal]");
  if (closeVideoTarget) closeVideoModal();

  const moduleTarget = event.target.closest("[data-module]");
  if (moduleTarget) {
    state.module = moduleTarget.dataset.module;
    render();
    return;
  }

  const openCreatorTarget = event.target.closest("[data-open-creator]");
  if (openCreatorTarget) {
    openModal();
    return;
  }

  const openSkuTarget = event.target.closest("[data-open-sku]");
  if (openSkuTarget) {
    openSkuModal();
    return;
  }

  const editSkuTarget = event.target.closest("[data-edit-sku]");
  if (editSkuTarget) {
    openEditSkuModal(editSkuTarget.dataset.editSku);
    return;
  }

  const viewVideosTarget = event.target.closest("[data-view-videos]");
  if (viewVideosTarget) {
    openVideoModal(viewVideosTarget.dataset.viewVideos);
    return;
  }

  const editVideoTarget = event.target.closest("[data-edit-video]");
  if (editVideoTarget) {
    openEditVideo(editVideoTarget.dataset.editVideo);
    return;
  }

  const deleteVideoTarget = event.target.closest("[data-delete-video]");
  if (deleteVideoTarget) {
    if (window.confirm(t("confirmDeleteVideo"))) {
      const shouldRefreshVideoModal = videoModal.classList.contains("open") && Boolean(state.activeVideoCreatorId);
      deleteVideoRecord(deleteVideoTarget.dataset.deleteVideo);
      render();
      if (shouldRefreshVideoModal) openVideoModal(state.activeVideoCreatorId);
      showToast(t("deleted"));
    }
    return;
  }

  const roiModeTarget = event.target.closest("[data-roi-mode]");
  if (roiModeTarget) {
    state.roiMode = roiModeTarget.dataset.roiMode;
    render();
    return;
  }

  const importTarget = event.target.closest("[data-import-creators]");
  if (importTarget) {
    document.querySelector("#creatorImportInput")?.click();
    return;
  }

  const editTarget = event.target.closest("[data-edit-creator]");
  if (editTarget) {
    openEditModal(editTarget.dataset.editCreator);
    return;
  }

  const deleteTarget = event.target.closest("[data-delete-creator]");
  if (deleteTarget) {
    if (window.confirm(t("confirmDelete"))) {
      deleteCreator(deleteTarget.dataset.deleteCreator);
      render();
      showToast(t("deleted"));
    }
    return;
  }

  const actionTarget = event.target.closest("[data-action]");
  if (actionTarget) showToast(`${actionTarget.dataset.action}: ${t("reserved")}`);
});

document.body.addEventListener("keydown", (event) => {
  if (event.key !== "Enter" && event.key !== " ") return;
  const actionTarget = event.target.closest("[data-action]");
  if (!actionTarget) return;
  event.preventDefault();
  showToast(`${actionTarget.dataset.action}: ${t("reserved")}`);
});

document.body.addEventListener("change", (event) => {
  if (event.target.id !== "creatorImportInput") return;
  const file = event.target.files?.[0];
  if (!file) return;
  if (file.name.toLowerCase().endsWith(".xlsx")) {
    showToast(t("unsupportedExcelFile"));
    event.target.value = "";
    return;
  }
  const reader = new FileReader();
  reader.onload = () => {
    const rows = parseDelimitedCreators(String(reader.result || ""));
    const count = importCreatorsFromRows(rows);
    creators = loadCreators();
    render();
    showToast(count ? `${t("importSuccess")}: ${count}` : t("importEmpty"));
  };
  reader.readAsText(file, "utf-8");
  event.target.value = "";
});

