function loadCreators() {
  try {
    const saved = JSON.parse(localStorage.getItem("creatorOpsCreators") || "[]");
    const deleted = JSON.parse(localStorage.getItem("creatorOpsDeletedCreators") || "[]");
    const normalizedSaved = normalizeSavedCreators(saved);
    if (JSON.stringify(saved) !== JSON.stringify(normalizedSaved)) {
      localStorage.setItem("creatorOpsCreators", JSON.stringify(normalizedSaved));
    }
    const overrides = new Map(normalizedSaved.filter((creator) => creator.originalName).map((creator) => [creator.originalName, creator]));
    const baseWithOverrides = baseCreators.map((creator) => {
      const override = overrides.get(creator.name);
      return override || { ...creator, id: baseCreatorId(creator.name) };
    });
    const custom = normalizedSaved.filter((creator) => !creator.originalName);
    return [...baseWithOverrides, ...custom].filter((creator) => !deleted.includes(creator.id) && !deleted.includes(creator.name) && !deleted.includes(creator.originalName));
  } catch {
    return baseCreators.map((creator) => ({ ...creator, id: baseCreatorId(creator.name) }));
  }
}

